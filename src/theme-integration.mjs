import { writeFileSync, mkdirSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))

/**
 * Search tokenColors for the first rule matching any of the given TextMate scopes.
 * Returns the foreground hex string, or null.
 */
function extractTokenColor(tokenColors, targetScopes) {
  for (const rule of tokenColors) {
    const scopes = Array.isArray(rule.scope)
      ? rule.scope
      : typeof rule.scope === 'string'
        ? [rule.scope]
        : []
    const hit = scopes.some(s =>
      targetScopes.some(t => s === t || s.startsWith(t + '.'))
    )
    if (hit && rule.settings?.foreground) return rule.settings.foreground
  }
  return null
}

export function themeIntegration() {
  return {
    name: 'halcyon-theme-integration',
    hooks: {
      'astro:config:setup': async ({ logger }) => {
        logger.info('Generating theme CSS and theme list...')

        const { bundledThemes } = await import('shiki')

        const themeList = []
        const cssBlocks = []

        for (const [name, loader] of Object.entries(bundledThemes)) {
          try {
            const { default: theme } = await loader()
            // Some themes use `settings` (old TextMate format), others `tokenColors`
            const tokenColors = theme.tokenColors || theme.settings || []

            const bg = theme.colors?.['editor.background']
              || '#1a1a1a'
            const fg = theme.colors?.['editor.foreground']
              || theme.colors?.['foreground']
              || '#f8f8f2'

            const accent = extractTokenColor(tokenColors, [
              'keyword'//, 'storage.type', 'keyword.control',
              //'entity.name.function', 'support.function',
            ]) || fg

            const accent2 = extractTokenColor(tokenColors, [
              //'string', 'string.quoted.double',
              //'string.quoted.single', 'string.template',
              'entity.name.function'
            ]) || accent

            const muted = extractTokenColor(tokenColors, [
              //'comment', 'comment.line',
              //'comment.block', 'comment.block.documentation',
              'variable'
            ]) || fg

            // swatch: three distinct colours for the panel preview dots
            themeList.push({ name, bg, swatch: [accent, accent2, muted] })

            cssBlocks.push(
              `:root[data-theme="${name}"] {\n` +
              `  --bg: ${bg};\n` +
              `  --fg: ${fg};\n` +
              `  --accent: ${accent};\n` +
              `  --accent-2: ${accent2};\n` +
              `  --muted: ${muted};\n` +
              `}`
            )
          } catch (e) {
            logger.warn(`[theme-integration] Skipping "${name}": ${e.message}`)
          }
        }

        // Write public/themes.css
        const publicDir = resolve(__dirname, '..', 'public')
        mkdirSync(publicDir, { recursive: true })
        writeFileSync(
          resolve(publicDir, 'themes.css'),
          '/* Auto-generated by theme-integration.mjs — do not edit */\n\n' +
          cssBlocks.join('\n\n') + '\n'
        )

        // Write src/generated/theme-list.json
        const generatedDir = resolve(__dirname, 'generated')
        mkdirSync(generatedDir, { recursive: true })
        writeFileSync(
          resolve(generatedDir, 'theme-list.json'),
          JSON.stringify(themeList, null, 2) + '\n'
        )

        logger.info(`[theme-integration] Generated ${themeList.length} themes`)
      },
    },
  }
}
